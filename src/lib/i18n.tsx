export type I18nKey = keyof (typeof messages)['ru'];

type I18nContextValue = {
  language: AppLanguage;
  setLanguage: (next: AppLanguage) => Promise<void>;
  t: (key: I18nKey, params?: TranslationParams) => string;
  formatDurationValue: (ms: number) => string;
  formatDateValue: (ymd: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>('ru');

  useEffect(() => {
    (async () => {
      const stored = await getAppLanguage();
      setLanguageState(stored);
    })();
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = useCallback(async (next: AppLanguage) => {
    setLanguageState(next);
    await setAppLanguage(next);
  }, []);

  const t = useCallback(
    (key: I18nKey, params?: TranslationParams) => {
      const source = messages[language][key] ?? messages.en[key] ?? messages.ru[key] ?? String(key);
      return interpolate(source, params);
    },
    [language],
  );

  const formatDurationValue = useCallback((ms: number) => formatDuration(ms, language), [language]);

  const formatDateValue = useCallback((ymd: string) => formatDate(ymd, language), [language]);

  const value = useMemo(
    () => ({ language, setLanguage, t, formatDurationValue, formatDateValue }),
    [language, setLanguage, t, formatDurationValue, formatDateValue],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return ctx;
}
